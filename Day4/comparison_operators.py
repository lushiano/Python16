# equals, not comparison, just one
my_variable = 'hello world'

# comparison, two equals
my_bool = 10 == 25
print(my_bool)

# sum up
my_bool = 20+5 == 25
print(my_bool)

# strings
my_bool = "white" == "White".lower()
print(my_bool)

my_bool = 100 == '100'
print(my_bool)

my_bool = 100.0 == 100
print(my_bool)

# difference
my_bool = 100.0 != 99
print(my_bool)

# greater
my_bool = 100.0 > 99
print(my_bool)

# less
my_bool = 100.0 < 99
print(my_bool)

# greater or equal
my_bool = 5 >= 5 
print(my_bool)
